using API.Data;
using API.DTOs;
using API.Entities;
using API.Interfaces;
using AutoMapper;
using AutoMapper.QueryableExtensions;
using Microsoft.EntityFrameworkCore;

namespace API.Repositories
{
    public class UserRepository : IUserRepository
    {
        private readonly DataContext _context;
        private readonly IMapper _mapper;

        public UserRepository(DataContext context, IMapper mapper)
        {
            _context = context;
            _mapper = mapper;
        }

        public async Task<CustomerDto> GetCustomerByIdAsync(int id)
        {
            return await _context.Users.Where(x => x.Id == id).ProjectTo<CustomerDto>(_mapper.ConfigurationProvider).SingleOrDefaultAsync();
        }

        public async Task<List<CustomerDto>> GetCustomersAsync()
        {
           return await _context.Users.ProjectTo<CustomerDto>(_mapper.ConfigurationProvider).AsNoTracking().ToListAsync();
        }

        public async Task<AppUser> GetUserByIdAsync(int id)
        {
            return await _context.Users.FindAsync(id);
        }

        public async Task<bool> SaveAllAsync()
        {
            return await _context.SaveChangesAsync() > 0;
        }

        public void Update(AppUser user)
        {
            _context.Entry(user).State = EntityState.Modified;
        }
    }
}
